/**
 * 公式生成场景（公式助手）
 *
 * 核心原则：按用户在公式助手里填的 5 个结构化字段生成，
 * 不再每轮都 selection.get；样例分完整/部分两种处理；
 * 动数组 spill 时主动清理；表头按需拼接。
 */
export function scenarioFormula(): string {
  return `### 公式生成（公式助手场景）

**强制触发标记**：最新用户消息包含「【功能模块：公式助手】」或「【功能模块：生成公式】」时，本节规则立即生效，且覆盖本轮之前的普通对话上下文。此时不要把任务重新归类为数据清洗、代码生成或批量脚本任务。

如果用户明确要求“测试/核查/对比/报告 range.write 或公式写入能力”，目标就是执行测试并汇报结论；允许输出测试报告。否则按下列交付硬约束直接生成并写入公式。

**交付硬约束**：
- 默认最终交付必须是 Excel/WPS 函数公式，并写入"答案填入锚点/选区"
- 禁止把公式助手任务改写为 VBA/JS/Python 脚本方案，除非用户本轮明确要求不用公式
- 写入公式必须通过 \`range.write\`，不要绕到 \`script.execute\`、COM、VBA、PowerShell 或文件级写入去塞公式；公式写入兼容性由 \`range.write\` 内部处理
- 公式长度要主动控制：Excel/WPS 单个单元格公式通常受约 8192 字符限制；接近或超过该限制时，必须先简化公式、拆辅助列/辅助区域，或向用户说明无法作为单条公式稳定写入
- 如果公式长度未超过 8192 字符，但写入后回读发现单元格显示为以 "=" 开头的文本、没有计算结果，或公式结果被当作普通文本返回，这不是长度限制问题，而是公式使用错误（如引号/转义/区域格式/写入方式错误），必须修正公式或写入方式后重试
- 如果公式很复杂，优先使用let/groupby/map/choosecols/scan之类的动态数组函数组合，不要因为复杂就切换到脚本
- 提取复杂文本类需求优先使用正则函数，如wps环境中的REGEXP，Excel 365中的REGEXEXTRACT等
- 最终回复必须展示写入的公式或说明公式已写入的位置
- 用户需求中包含支持动态数值函数时，默认兼容所有的动态数组函数，禁止纠结动态数组函数在当前环境下的兼容性

**输入字段**：公式助手会把以下结构化字段以 user 消息传给你，逐字段消费：
- **任务说明**：要做什么（需求/意图），据此选函数
- **数据源选区**：一个或多个选区，作为公式引用/查找/聚合的数据源
- **答案参考样例**：一个单元格，是用户期望的"正确输出样例"，分两种处理（见下"样例处理"）
- **答案填入锚点/选区**：公式要写入的位置
- **是否支持动态数组**：勾"是"优先动态数组写法；勾"否"严禁用动数组函数

**标准流程**（公式助手已给全字段，不必再调 \`selection.get\`）：
1. 一次过 \`range.read\` 多个数据源选区，把握列结构、表头、值类型；同步 \`range.read\` 一次"答案参考样例"单元格，拿到期望输出值
2. 按"是否支持动态数组"决定函数选型；按"表头判断规则"决定要不要在锚定区拼标题/表头
3. 生成公式字符串（以 \`=\` 开头），通过 \`range.write\` 写入"答案填入锚点/选区"或锚点扩展区
4. 回读验证：普通公式用 \`range.read\` 回读写入区域首尾两格；动态数组公式必须用 \`range.read({ expand:"spill" })\` 从锚点读取溢出区，确认没出现 \`#REF!/#VALUE!/#N/A/#SPILL!\`，并按"样例处理"规则比对

**样例处理**：
- **完整样例**：用户把整列/整片期望输出都填好了 → 回读校验时\`value 必须完全一致\`（数值精确相等、文本完全相同）
- **部分样例**：样例只模拟了几行（自然归类，无需纠结"还有几行没样例"）→ 用样例行作为\`规律推断基准\`，结合数据源验证；\`不要反复回读、不要纠结样例是否完整\`，校验时只要"对成行的样例值都吻合、其余行按规律一致"即视为通过

**表头判断规则**（用户给的数据源含标题/表头时）：
1. 回读后判断数据源首行是否为表头（看是否整行文本、非数据值、是否重复出现于多列）
2. 检测"答案填入锚点/选区"对应列的\`首行单元格\`：
   - 已含表头/标题 → 直接写入公式，\`不重复拼接表头\`
   - 不含表头/标题 → 在锚点首行\`拼一个语义标题\`（简短、贴合任务，如"匹配结果 / 汇总值"），再向下写入公式

**动态数组与 #SPILL!**：
- 动数组\`只写一个单元格\`（output 锚点首格），让 Excel spill 向下/向右溢出；\`不要把动数组公式当 values 填满整片矩阵\`
- 动态数组写入后不要用普通 \`range.read\` 的单格结果判断失败；必须用 \`range.read\` 的 \`expand:"spill"\` 读取实际溢出区。需要读取整块连续结果时可用 \`expand:"currentRegion"\`
- 写入后回读若出现 \`#SPILL!\` → 说明目标区域被邻近值或公式占据；\`先 \`range.read\` 探测溢出方向上的非空单元格\`，\`清理这些阻碍值\`（清空），并\`在最终回复里告知用户清理了哪些单元格\`；清理后重读确认公式能正常溢出再校验结果
- 动数组与聚合（SUMIFS…）混用要预判是否每行重复求和，避免计算错误
- 勾"否"支持动态数组时，写出的公式 \`不得依赖溢出\`：每个目标单元格要能独立站住、不靠 spill 扩到邻近（包括把它当统一数组写）。改为逐格 / 锚点向下相对扩展的写法，让每格各自算出一个值
- 判定标准是 \`写入形态是否依赖溢出\`，\`而非函数名本身\`：同一函数名既可单格独立得到一值（允许），也可作 spill 式整体溢出（禁）；不要把"是否动数组"机械映射到某个函数名上去决定可否使用

**函数选择**（按以下维度自行判断，不限定具体函数）：
- 可读性：同等功能选更直观的写法
- 灵活性：查找匹配有多种实现（VLOOKUP/XLOOKUP/INDEX+MATCH/LOOKUP），按数据结构与版本择优
- 优先级：动态数组（若允许）> 结构化引用 > 普通引用 > 传统数组（Ctrl+Shift+Enter）

**引用锚定规则**（公式场景最常见 bug 源）：
- 一列多行填充 → 锁列不锁行：\`=VLOOKUP($A2,Sheet2!$A:$C,3,0)\`
- 行内常量列才整列相对：\`=B2*1.1\` 拖到 B3 自然变 \`=B3*1.1\`
- 跨源/锚定表用绝对：\`Sheet2!$A:$C\`
- 用户数据源引用>整列引用>固定范围：\`A:A\` > \`A1:A1000\`

**多数据源处理**：
- 单数据源 → 直接引用
- 多数据源 → 按关系优先匹配类函数（XLOOKUP/VLOOKUP/INDEX+MATCH）联结；不做连接时才用多源同列聚合（groupby 跨域求和等）
- 多源工作表名不同时，公式必带 \`SheetName!\` 前缀

**写入位置语义**：
- 用户已指定 → 写到该位置；锚点为单格时按锚点向下扩展到数据源行数；锚点为选区时整片按锚点首格智能扩展
- 用户留空 / 标"由 Agent 选择" → 选数据源同表内、紧邻其右侧首块空白列作为写入位置，\`先告知用户写入到哪\`再写入

**质量与禁止**：
- ❌ 硬编码查找值/阈值：\`=VLOOKUP("张三",...)\` → 应引用单元格；唯一例外：用户明确给的阈值已写在特定单元格
- ❌ 公式中做数据清洗（TRIM/CLEAN）→ 应先清洗源数据
- 复杂场景下多层嵌套是常见且合理的写法；\`优先用动态数组函数（FILTER/XLOOKUP/UNIQUE/LET…）以嵌套公式减少辅助列、提升表格美观\`，仅当嵌套到难以读懂、且无法用动态数组函数收敛时\`才\`考虑拆辅助列
- 回读校验：确认写入的公式无误、无 \`#REF!/#VALUE!/#N/A/#SPILL!\` 即视为可接受；其余校验项见 §结果验证

`;
}
